    <h2>Article 1 – Objet et champ d'application</h2>
    <p>
      Les présentes conditions générales ont pour objet de définir les modalités
      d'exécution des travaux d'assainissement, de curage, de pompage, de débouchage
      et de maintenance réalisés par la société SAHP (ci-après « le Prestataire »)
      pour le compte de clients professionnels ou particuliers (ci-après « le Client »).
    </p>
    <p>
      Toute signature de devis ou acceptation d'intervention implique l'acceptation
      sans réserve des présentes CGPS.
    </p>

    <h2>Article 2 – Devis et commandes</h2>
    <p>
      <strong>2.1.</strong> Toute intervention fait l'objet d'un devis préalable gratuit,
      valable pour une durée de 30 jours, sauf mention contraire.
    </p>
    <p>
      <strong>2.2.</strong> La commande est considérée comme définitive dès réception
      du devis signé avec la mention « Bon pour accord », ou d'un ordre de service écrit
      (email, bon de commande).
    </p>
    <p>
      <strong>2.3.</strong> Interventions d'urgence : en cas d'urgence absolue
      (inondation, débordement), ne permettant pas l'établissement d'un devis préalable,
      l'ordre de réparation signé sur place par le Client avant le début des travaux
      vaut acceptation des tarifs en vigueur.
    </p>

    <h2>Article 3 – Prix et modalités de paiement</h2>
    <p>
      <strong>3.1.</strong> Les prix sont libellés en euros et calculés hors taxes (HT).
      Ils sont majorés du taux de TVA en vigueur au jour de la facturation.
    </p>
    <p>
      <strong>3.2.</strong> Les interventions réalisées de nuit (19h–7h), les week-ends
      et les jours fériés font l'objet de majorations tarifaires précisées sur le devis
      ou la grille tarifaire.
    </p>
    <p>
      <strong>3.3.</strong> Sauf conditions particulières indiquées sur le devis :
    </p>
    <ul>
      <li>Pour les particuliers : paiement comptant à la fin de l'intervention.</li>
      <li>Pour les professionnels : paiement à 30 jours date de facture, sauf accord contraire.</li>
    </ul>
    <p>
      <strong>3.4.</strong> Tout retard de paiement entraînera l'exigibilité immédiate
      des sommes dues, l'application de pénalités de retard au taux de trois fois le
      taux d'intérêt légal, ainsi qu'une indemnité forfaitaire de 40 € pour frais de
      recouvrement (article D.441-5 du Code de commerce).
    </p>

    <h2>Article 4 – Obligations du Client (accès et sécurité)</h2>
    <p>
      <strong>4.1.</strong> Le Client s'engage à assurer aux équipes de SAHP un accès
      libre et sécurisé aux installations.
    </p>
    <p>
      <strong>4.2.</strong> Le Client doit signaler, avant l'intervention, la présence
      éventuelle de produits dangereux ou chimiques dans les canalisations.
    </p>
    <p>
      <strong>4.3.</strong> En cas d'impossibilité ou d'interruption d'intervention du
      fait du Client, le déplacement et le temps passé seront facturés selon le tarif
      en vigueur.
    </p>

    <h2>Article 5 – Exécution des travaux et imprévus</h2>
    <p>
      <strong>5.1.</strong> SAHP est tenue à une obligation de moyens et met en œuvre
      les techniques adaptées pour résoudre le problème.
    </p>
    <p>
      <strong>5.2.</strong> En cas d'anomalies structurelles découvertes en cours
      d'intervention (cassure, affaissement, racines, objets anormaux), le Client sera
      immédiatement informé. Ces travaux feront l'objet d'un devis complémentaire.
    </p>

    <h2>Article 6 – Gestion des déchets</h2>
    <p>
      SAHP assure l'acheminement des déchets issus des prestations de pompage vers un
      centre de traitement agréé. Un Bordereau de Suivi des Déchets (BSD) peut être remis
      au Client sur demande. Les frais de traitement sont à la charge du Client.
    </p>

    <h2>Article 7 – Responsabilité et assurances</h2>
    <p>
      <strong>7.1.</strong> SAHP ne pourra être tenue responsable des dommages liés à la
      vétusté ou à l'état des installations du Client.
    </p>
    <p>
      <strong>7.2.</strong> SAHP est couverte par une assurance responsabilité civile
      professionnelle auprès d'une compagnie notoirement solvable.
    </p>

    <h2>Article 8 – Droit de rétractation (clients particuliers)</h2>
    <p>
      <strong>8.1.</strong> Le Client particulier dispose d'un délai légal de 14 jours
      pour exercer son droit de rétractation sur un contrat conclu à distance.
    </p>
    <p>
      <strong>8.2.</strong> Conformément à l'article L.221-28 du Code de la consommation,
      ce droit ne s'applique pas aux interventions d'urgence expressément sollicitées.
    </p>

    <h2>Article 9 – Loi applicable et juridiction compétente</h2>
    <p>
      Les présentes conditions sont soumises au droit français. À défaut de résolution
      amiable, tout litige sera de la compétence exclusive du Tribunal de Commerce de
      <strong>Créteil</strong>.
    </p>

